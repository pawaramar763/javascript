var sweety = "Sweety",
  cutie = "Cutie";
console.log(
  "Values before swapping:",
  "first name=",
  sweety,
  ":second name=",
  cutie
);
var temp = sweety;
sweety = cutie;
cutie = temp;
console.log(
  "Values after swapping:",
  "first name=",
  sweety,
  ":second name=",
  cutie
);
console.log(
  "======================================================================="
);

